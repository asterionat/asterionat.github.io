function initialLanguage(){
	if(navigator.language.toUpperCase().includes()=='ES') locale = localeES;
	else locale = localeEN;
}

function changeLanguage(lang){
	if(lang=='ES') locale = localeES;
	if(lang=='EN') locale = localeEN;
}

function applyLanguage(){
	var elements = document.getElementsByTagName("*");
	//console.log(elements.length);
	for (i = 0; i < elements.length; i++){
		if (elements[i].id.includes("locale.")) {
			elements[i].innerHTML = eval(elements[i].id.valueOf());
			//console.log(i,'-',elements[i].id,'-',elements[i].innerHTML);
		}  
	}
}

function clickLanguage(){
	if(locale.language=='ES') this.changeLanguage('EN');
	else this.changeLanguage('ES');
	this.applyLanguage();
	//alert('Cambio a: '+locale.language);
}

